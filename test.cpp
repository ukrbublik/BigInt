#include "bigint.h"
#include <conio.h>

int main()
{
	//TIP: results can be checked with PHP's bcmath lib, for example
	BigInt a("6532764532547857900675090904854854850854673"), b("32362548628636543646545353");
	assert((a + b) == "6532764532547857933037639533491398497400026");
	assert((a - b) == "6532764532547857868312542276218311204309320");
	assert((a * b) == "211416909864012129805401785310389961568330653581337773425035906484569");
	assert((a / b) == "201861868405729692");
	assert((a % b) == "19688186967898047914133397");
	return 0;
}
